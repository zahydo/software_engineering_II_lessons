    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        List<Category> listaCategorias = new ArrayList<>();

        try {
            if (this.rdoId.isSelected()) {
                listaCategorias.add(categoriaService.findCategoryById(Long.parseLong(this.txtSearch.getText())));
                fillTable(listaCategorias);
            }

            if (this.rdoName.isSelected()) {
                listaCategorias.add(categoriaService.findCategoryByName(this.txtSearch.getText()));
                fillTable(listaCategorias);
            }
        }catch(Exception e){
            Messages.showMessageDialog("Error. Revisa que hayas ingresado bien los datos", "Atenci√≥n");

        }

    }//GEN-LAST:event_btnSearchActionPerformed

