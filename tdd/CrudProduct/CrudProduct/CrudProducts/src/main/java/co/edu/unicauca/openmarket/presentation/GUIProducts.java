    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (txtName.getText().trim().equals("")) {
            Messages.showMessageDialog("Debe ingresar el nombre del producto", "Atenci贸n");
            txtName.requestFocus();
            return;
        }
        
        if(txtIdCategoria.getText().equals("")){
            Messages.showMessageDialog("Debe ingresar el id de la categoria", "Atenci贸n");
            txtIdCategoria.requestFocus();
            return;
        }
        
        try{
            if (addOption) {
                //Agregar
                addProduct();
            } else {
                //Editar
                editProduct();
            } 
        }catch(Exception e){
               Messages.showMessageDialog("Error. Revisa que hayas ingresado bien los datos", "Atenci贸n");
        }
   

    }//GEN-LAST:event_btnSaveActionPerformed

