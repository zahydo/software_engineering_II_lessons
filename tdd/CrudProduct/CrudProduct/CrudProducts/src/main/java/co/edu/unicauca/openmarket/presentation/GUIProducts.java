    private void cleanControls() {
        txtId.setText("");
        txtName.setText("");
        txtDescription.setText("");
        txtIdCategoria.setText("");
    }

    private void addProduct() {
        String name = txtName.getText().trim();
        String description = txtDescription.getText().trim();
        Long id_category = Long.parseLong(txtIdCategoria.getText());
        Category categoria = categoryService.findCategoryById(id_category);
        if(categoria!=null){
            if (productService.saveProduct(name, description, categoria)) {
                Messages.showMessageDialog("Se grab贸 con 茅xito", "Atenci贸n");
                cleanControls();
                stateInitial();
            } else {
                Messages.showMessageDialog("Error al grabar, lo siento mucho", "Atenci贸n");
            }
        }else{
            Messages.showMessageDialog("Error al grabar, lo siento mucho", "Atenci贸n");

        }
    }

    private void editProduct() {
        String id = txtId.getText();
        if (id.equals("")) {
            Messages.showMessageDialog("Debe buscar el producto a editar", "Atenci贸n");
            txtId.requestFocus();
            return;
        }
        Long productId = Long.parseLong(id);
        Product prod = new Product();
        prod.setName(txtName.getText().trim());
        prod.setDescription(txtDescription.getText().trim());

        if (productService.editProduct(productId, prod)) {
            Messages.showMessageDialog("Se edit贸 con 茅xito", "Atenci贸n");
            cleanControls();
            stateInitial();
        } else {
            Messages.showMessageDialog("Error al editar, lo siento mucho", "Atenci贸n");
        }
    }
